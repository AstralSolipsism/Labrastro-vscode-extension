import { describe, expect, it } from "vitest"
import { isHostToWebviewMessage, isWebviewToHostMessage } from "./messages"

describe("protocol message guards", () => {
  it("accepts known host/webview messages", () => {
    expect(isHostToWebviewMessage({ type: "sessionRun.started", text: "hi" })).toBe(true)
    expect(isHostToWebviewMessage({ type: "session.loaded", sessionId: "s1" })).toBe(true)
    expect(isHostToWebviewMessage({ type: "navigate", view: "taskflow", taskflowId: "tf-1" })).toBe(true)
    expect(isHostToWebviewMessage({ type: "taskflow.focusChatInteraction", taskflowId: "tf-1" })).toBe(true)
    expect(isHostToWebviewMessage({ type: "taskflow.state", taskflowId: "tf-1" })).toBe(true)
    expect(isHostToWebviewMessage({ type: "taskflow.workspace", taskflowId: "tf-1" })).toBe(true)
    expect(isHostToWebviewMessage({ type: "taskflow.projectMemory", taskflowId: "tf-1" })).toBe(true)
    expect(isHostToWebviewMessage({ type: "taskflow.projectorPreview", taskflowId: "tf-1" })).toBe(true)
    expect(isHostToWebviewMessage({ type: "taskflow.runtime", taskflowId: "tf-1" })).toBe(true)
    expect(isHostToWebviewMessage({ type: "taskflow.action.error", taskflowId: "tf-1" })).toBe(true)
    expect(isHostToWebviewMessage({ type: "modelCapabilities.state", payload: { ok: true } })).toBe(true)
    expect(isHostToWebviewMessage({ type: "modelCapabilities.error", message: "failed" })).toBe(true)
    expect(isHostToWebviewMessage({ type: "peerDiagnosticsLogging.state", payload: { enabled: true } })).toBe(true)
    expect(isHostToWebviewMessage({ type: "chat.sendDuringRunMode.state", payload: { mode: "guide" } })).toBe(true)
    expect(isHostToWebviewMessage({ type: "workspace.files", query: "src", files: ["src/index.ts"] })).toBe(true)
    expect(isWebviewToHostMessage({ type: "chat.send", text: "hi" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "chat.command.dispatch", text: "/help" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "sessionRun.followup", sessionRunId: "run-1", text: "guide" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "sessionRun.followup.cancel", sessionRunId: "run-1", followupId: "f1" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "sessionRun.recover", sessionRunId: "run-1", action: "continue" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "chat.sendDuringRunMode.update", mode: "queue" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "workspace.files.search", query: "src" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "session.load", sessionId: "s1" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "openTaskflow", taskflowId: "tf-1" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "taskflow.focusChatInteraction", taskflowId: "tf-1" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "taskflow.state.get", taskflowId: "tf-1" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "taskflow.workspace.get", taskflowId: "tf-1" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "taskflow.runtime.get", taskflowId: "tf-1" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "taskflow.reviewCardV1.action", taskflowId: "tf-1" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "taskflow.projectMemory.patch.preview", taskflowId: "tf-1" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "taskflow.compilerDecision.review", taskflowId: "tf-1" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "taskflow.projectorPreview.get", taskflowId: "tf-1" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "taskflow.brief.compile", taskflowId: "tf-1" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "taskflow.brief.ready", taskflowId: "tf-1" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "taskflow.brief.confirm", taskflowId: "tf-1" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "taskflow.goal.compile", taskflowId: "tf-1" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "taskflow.dispatch.request", taskflowId: "tf-1" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "taskflow.dispatch.confirm", taskflowId: "tf-1" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "taskflow.dispatch.reject", taskflowId: "tf-1" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "taskflow.workItem.dispatch", taskflowId: "tf-1" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "modelCapabilities.status" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "modelCapabilities.list", payload: { provider: "deepseek" } })).toBe(true)
    expect(isWebviewToHostMessage({ type: "modelCapabilities.refresh" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "modelCapabilities.apply", payload: { profile_id: "main" } })).toBe(true)
    expect(isWebviewToHostMessage({ type: "peerDiagnosticsLogging.get" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "peerDiagnosticsLogging.save", payload: { enabled: true } })).toBe(true)
    expect(isWebviewToHostMessage({ type: "peerDiagnosticsLogging.open" })).toBe(true)
    expect(isWebviewToHostMessage({ type: "peerDiagnosticsLogging.clear" })).toBe(true)
  })

  it("rejects unknown message types at the bridge boundary", () => {
    expect(isHostToWebviewMessage({ type: "unknown.host" })).toBe(false)
    expect(isWebviewToHostMessage({ type: "unknown.webview" })).toBe(false)
    expect(isWebviewToHostMessage({})).toBe(false)
    expect(isWebviewToHostMessage(null)).toBe(false)
  })
})
